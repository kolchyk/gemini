"use client";

import { memo } from "react";
import { Card, CardContent, CardHeader, CardTitle } from "@/components/ui/card";
import { Button } from "@/components/ui/button";
import {
  Collapsible,
  CollapsibleContent,
  CollapsibleTrigger,
} from "@/components/ui/collapsible";
import { Skeleton } from "@/components/ui/skeleton";
import type { GenerationResult } from "@/lib/types";

const MODEL_DISPLAY_NAMES: Record<string, string> = {
  "gemini-3.1-flash-image-preview": "⚡ Flash",
  "gemini-3-pro-image-preview": "✨ Pro",
};

interface ResultSectionProps {
  results: Record<string, GenerationResult> | null;
  isGenerating: boolean;
}

function downloadImage(base64: string, filename: string) {
  const byteString = atob(base64);
  const bytes = new Uint8Array(byteString.length);
  for (let i = 0; i < byteString.length; i++) {
    bytes[i] = byteString.charCodeAt(i);
  }
  const blob = new Blob([bytes], { type: "image/png" });
  const url = URL.createObjectURL(blob);
  const a = document.createElement("a");
  a.href = url;
  a.download = filename;
  a.click();
  URL.revokeObjectURL(url);
}

export const ResultSection = memo(function ResultSection({
  results,
  isGenerating,
}: ResultSectionProps) {
  if (isGenerating) {
    return (
      <Card>
        <CardHeader>
          <CardTitle className="text-lg">🖼️ Крок 3: Результат</CardTitle>
        </CardHeader>
        <CardContent>
          <div className="grid grid-cols-1 md:grid-cols-2 gap-4">
            <div className="space-y-3">
              <Skeleton className="w-full aspect-square rounded-md" />
              <Skeleton className="h-10 w-full" />
            </div>
          </div>
          <p className="text-center text-muted-foreground mt-4 animate-pulse">
            ✨ Генеруємо ваш шедевр...
          </p>
        </CardContent>
      </Card>
    );
  }

  if (!results) {
    return (
      <div className="text-center py-16 px-8 bg-gradient-to-br from-accent/50 to-transparent border border-dashed rounded-2xl my-8">
        <span className="text-6xl block mb-4 animate-bounce">🍌</span>
        <h3 className="text-lg font-semibold mb-2">Результат з&apos;явиться тут</h3>
        <p className="text-muted-foreground max-w-sm mx-auto">
          Налаштуйте промпт та натисніть &quot;Згенерувати зображення&quot;
        </p>
      </div>
    );
  }

  const entries = Object.entries(results);

  return (
    <Card>
      <CardHeader>
        <CardTitle className="text-lg">🖼️ Крок 3: Результат</CardTitle>
      </CardHeader>
      <CardContent>
        <div className={`grid gap-4 ${entries.length > 1 ? "grid-cols-1 md:grid-cols-2" : "grid-cols-1 max-w-2xl mx-auto"}`}>
          {entries.map(([modelName, result]) => {
            const displayName = MODEL_DISPLAY_NAMES[modelName] || modelName;
            return (
              <div key={modelName} className="space-y-3">
                <p className="font-semibold">{displayName}</p>
                {result.image_base64 ? (
                  <>
                    <img
                      src={`data:image/png;base64,${result.image_base64}`}
                      alt={`Generated by ${displayName}`}
                      className="w-full rounded-md border"
                    />
                    <Button
                      className="w-full"
                      onClick={() =>
                        downloadImage(
                          result.image_base64!,
                          `generated_${modelName}_${Date.now()}.png`
                        )
                      }
                    >
                      📥 Завантажити ({displayName})
                    </Button>
                    {result.text_output && (
                      <Collapsible>
                        <CollapsibleTrigger className="text-xs text-muted-foreground hover:text-foreground transition-colors cursor-pointer">
                          Опис від {displayName}
                        </CollapsibleTrigger>
                        <CollapsibleContent>
                          <p className="text-sm text-muted-foreground mt-2 p-3 bg-muted rounded-md">
                            {result.text_output}
                          </p>
                        </CollapsibleContent>
                      </Collapsible>
                    )}
                  </>
                ) : (
                  <p className="text-amber-600 bg-amber-50 dark:bg-amber-950/30 p-3 rounded-md text-sm">
                    {result.error
                      ? `${displayName}: ${result.error}`
                      : `${displayName} не повернула зображення.`}
                  </p>
                )}
              </div>
            );
          })}
        </div>
      </CardContent>
    </Card>
  );
});
